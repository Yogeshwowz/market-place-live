					<h2 class="bg-gray" id="Benefits"> The Benefits of Buying Vs. Starting A Business</h2>
					<p>
						 So you want to be your own boss. Consider the options – work as an independent
						 contractor…start your own business…buy an existing company.
					 </p>
					 <p>
						 Certainly there are pros and cons to each option. If you do a careful analysis,
						 you’ll learn what many seasoned entrepreneurs have discovered…the risk-to-reward
						 ratio is tipped in your favor when you purchase an existing business.
					 </p>
					 <p>
						 Admittedly, as an independent contractor, your risk is minimal. The upfront  investment
						 and overhead costs are limited. However, without the ability to leverage the work
						 of an employee base, the returns are limited by your own personal capacity.
					 </p>
					 <p>
						 Starting a business of your own can pay great dividends, but it’s important
						 to understand that the risks are significant. Most start-up businesses will falter
						 and eventually die. According to Michael Gerber, author of The E-Myth Revisited,
						 40 percent of new businesses fail in the first year and 80 percent fail within five
						 years. On the other hand, purchasing an existing business reduces an entrepreneur’s
						 risk while creating opportunities for tremendous profit.
					 </p>
					 <p>
						 There are a number of reasons to consider the purchase of an existing business rather
						 than starting one:
					 </p>
					 <p>
						 <b>Proven Concept.</b> Buying an established business is less risky –
						 as a buyer you already know the process or concept works. Financing a purchase is
						 often easier than securing funding for a start-up business for that very reason—the
						 business has a track record. A bank will be able to look at the historical results
						 for the business, not just rely on projections.
					 </p>
										 <p>
							 <b>Brand. </b>You’re buying a brand name. The on-going benefits
							 of any marketing or networking the prior owner has done will transfer to you. When
							 you have an established name in the business community, it’s easier to place
							 cold calls and attract new business than with an unproven start-up. That’s
							 an intangible benefit that’s difficult to put a price on.
						 </p>
						 <p>
							 <b>Relationships. </b>With the purchase of an existing business, you will
							 also be buying an existing customer base and vendor base that took years to build.
							 It’s very common for the seller to stay on and transition with the business
							 for a short time to transfer those relationships to the buyer.
						 </p>
						 <p>
							 <b>Focus.</b> When you buy a business, you can start working immediately
							 and focus on improving and growing the business immediately. The seller has already
							 laid the foundation and taken care of the time-consuming, tedious start-up work.
							 Starting a new business means spending a lot of time and money on basic items like
							 computers, telephones, furniture and policies that don’t directly generate
							 cash flow.
						 </p>
						 <p>
							 <b>People.</b> In an acquisition, one of the most valuable and important
							 assets you’re buying is the people. It took the seller time to find those
							 employees, develop them and assimilate them into the company culture. With the right
							 team in place, just about anything is possible and you will have an easier time
							 implementing growth strategies. Plus, with trained people in place you will have
							 more liberty to take vacation, spend time with family, or work on other business
							 ventures. When start-up owners and independent contractors go on vacation, the business
							 goes too.
						 </p>
						 <p>
							 <b>cash flow. </b>Typically, a sale is structured so you can cover the
							 debt service, take a reasonable salary, and have some left over to take the business
							 to the next level. Start up owners, on the other hand, often “starve”
							 at first. Some experts say start-ups aren’t expected to make money for the
							 first three years.
						 </p>
						 <p>
							 <b>Risk</b>. Even with all these advantages, some entrepreneurs believe
							 it is cheaper, and therefore less risky, to start a business than to buy one. But
							 risk is relative. A buyer may pay $1 million, for example, for an established business
							 with strong cash flows of approximately $200,000 to $300,000. A lending institution
							 funds the transaction because historical revenues show the cash flow can support
							 the purchase price. For many people, however, that is far less risky than taking
							 out a $300,000 loan with an unproven concept and projections that may or may not
							 be realized.
						 </p>

						 <p>
							 Becoming your own boss always involves a risk. When you buy a business, you take
							 a calculated risk that eliminates a lot of the pitfalls and potential for failure
							 that come with a start-up.
						 </p>

						 <h2 class="bg-gray" id="buy2">
							 Business Buying Process
						 </h2>
						 <p>
							 Buying a business is a process that takes time. It can sometimes take years to find
							 the right opportunity.
						 </p>
						 <p>
							 Unfortunately, many buyers want to look at all available options, thinking they’ll
							 recognize what they’re looking for when they see it. That approach is actually
							 a waste valuable time and energy and can lead to frustration and an end to the search.
							 Or the potential buyer may miss out on great opportunities because they weren’t
							 found early enough or they weren’t ready to move forward with a purchase.
						 </p>
						 <p>
							 There are some key steps to follow in the business search process:
						 </p>
						 <p>
							 <b>Start with a self assessment: </b>
						
							 Ask yourself why you want to buy a business. What types of work activities do you
							 like and what kind of lifestyle do you want to pursue? It’s important to understand
							 that there may be more work and longer hours for an owner in some industries. Be
							 sure to include your family in the assessment.
						 </p>
						 <p>
							 <b>Establish financial expectations:</b>
						 
							 Determine how much money you need and want to earn. Make sure your expectations
							 are in line with the types of businesses you are targeting and the return they can
							 produce.
						 </p>
						 <p>
							 <b>Put together a personal financial statement: </b>
						 
							 Outline your assets and liabilities. Identify what you can use for your initial
							 investment. The personal financial statement serves as proof of your financial wherewithal,
							 so be prepared to share this document with a seller’s intermediary.
						 </p>
						 <p>
							 <b>Update your resume:</b>
						 
							 Sellers want to be sure that their business will continue to be a success. They’re
							 looking for someone with the experience necessary to continue their legacy and take
							 care of the staff. Ultimately, you’re selling yourself to the current owner(s),
							 the lender and the professionals representing them.
						 </p>
						 <p>
							 <b>Outline your acquisition criteria: </b>
						
							 Define the parameters of your search. Ideally it should include your targeted industries,
							 geographic area and transaction size. Your acquisition criteria will help you demonstrate
							 your commitment to finding the right business for you.
						 </p>
						 <p>
							 <b>Search multiple sources and enlist help: </b>
						
							 Let your professional advisors (e.g. attorney, accountant, financial planner) know
							 you are looking for a business. Most importantly, contact business intermediaries
							 who represent businesses within your targeted market. They will notify you of available
							 companies that meet your criteria and qualifications.
						 </p>
						 <p>
							 Most business brokers or intermediaries work for the seller and are paid by the
							 seller. That means you can enjoy the luxury of their services at no cost. The intermediary
							 is looking out for the seller’s best interests, so you should have experienced
							 council to represent you in any transaction.
						 </p>
						 <p>
							 When interested in a business, you want the business intermediary to be selling
							 you to the seller. Prove to them that you are a qualified, motivated buyer by preparing
							 for your search.
						 </p>
						 <p>
							 Your motivation, lifestyle, expectations, financial statement and résumé will help
							 you develop your acquisition criteria. Identifying and communicating your acquisition
							 criteria, qualifications and experience will save time and frustration and will
							 place you far ahead of less focused buyers.
						 </p>


						 <h2 class="bg-gray" id="buy3">
							 Due Diligence
						 </h2>
						 <p>
							 Merriam-Webster Dictionary defines due diligence as “research and analysis
							 of a company or organization done in preparation for a business transaction.”
							 Some even look at it as a pre-marital background check and counseling. But it should
							 be noted that dissolving a merger is much more difficult than ending a marriage
							 if things aren’t as they appear.
						 </p>
						 <p>
							 Ultimately, due diligence is the process of being sure that things are as they appear
							 before a deal is sealed. For someone considering a merger or the purchase of an
							 existing business, the review of documentation and the answers to your due diligence
							 questions are critical. There’s no doubt it is a complex process that can
							 be time-consuming. But with so much on the line with any merger or acquisition,
							 you don’t want to make a decision without all of the information. You want
							 to be sure everything is reviewed and all questions are answered to your satisfaction.
						 </p>
						 <p>
							 During the due diligence process, an often lengthy list of documents should be provided.
							 The list of documents should cover a range of areas, including:
						 </p>

						 <p>Legal structure and incorporation of the company</p>
						 <p>Internal Revenue Service (IRS) records</p>
						 <p>Insurance policy information</p>
						 <p>Organizational structure</p>
						 <p>Personnel policies • Operations</p>
						 <p>Capital and Real Estate</p>
						 <p>Contracts, licenses, agreements and affiliations</p>
						 <p>Technology and Intellectual Property </p>
						 <p>Current or potential legal liabilities</p>
						 <p>Marketing materials</p>

						 <p>
							 Today more than ever, buyers are putting more emphasis on the due diligence process.
							 And while the financial aspect is a key component, the due diligence process should
							 also consider organizational items. Be sure to seek documentation and ask important
							 questions about the company’s culture, strategy, leadership and competencies.
						 </p>
						 <p>
							 To properly address and evaluate all of the areas of the due diligence process,
							 you want to assemble the best possible team of people. Work with that team, including
							 your business intermediary, throughout the process to review and evaluate the documents
							 and information you receive. It’s also important to keep an open mind. Be
							 sure that you get all of the information you need, but don’t assume that you
							 will find something wrong.
						 </p>
						 <p>
							 Although the due diligence process may take considerable time, it’s a critical
							 part of any transaction and should be considered the foundation of the entire deal.
						 </p>

